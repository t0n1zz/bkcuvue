    <p>
        Terdapat juga tombol Surat Pencairan <span class="badge badge-pill bg-warning-400 ml-auto ml-md-0">1</span> yang berguna untuk mencetak surat pernyataan dari PUSKOPCUINA yang menjelaskan mengenai status bantuan solidaritas anggota yang telah diajukan.

        <br/><br/>
        Surat ini bisa digunakan dan diberikan kepada alih waris atau keluarga dari anggota CU sebagai bukti <b>apabila diminta.</b>
    </p>

    
