    <hr/> 
    <h6>A. Menambah Bantuan Solidaritas Jalinan</h6>
    
    <p>Terdapat beberapa cara untuk melakukan Bantuan Solidaritas Jalinan. Yaitu sebagai berikut:</p>

    <b>1. Menambah Bantuan Solidaritas Jalinan dengan mengisi No. KTP/NIK</b>
    <p>
      Cara pertama untuk menambah data bantuan solidaritas Jalinan adalah melalui tombol tambah pada bagian bantuan solidaritas Jalinan. Disini akan muncul pilihan yaitu memasukkan nomor identitas anggota/NIK (no. KTP) dan apabila data anggota tersebut ada maka akan langsung muncul form untuk mengisi informasi detail terkait klaim.

      <br/><br/>
      Tapi apabila tidak terdapat data anggota terhadap nomor identitas anggota/NIK (no. KTP) maka akan muncul tombol Tambah Anggota CU yang akan mengarahkan ke form untuk menambahkan anggota CU dan setelah selesai mengisi data anggota CU akan dibawa kembali ke form pengajuam bantuan solidaritas untuk melengkapi informasi detail terkait klaim.
    </p>

    <div class="mb-3 text-center">
      <img src="{{ asset('images/panduan/klaimJALINAN-tambah-ktp.png') }}" class="img-thumbnail" alt="">
    </div>

    <b>2. Menambah Bantuan Solidaritas Jalinan yang anggota tidak ditemukan lagi nomor identitas anggota/NIK (no. KTP)</b>
    <p>
      Apabila ternyata anggota yang akan mengajukan bantuan solidaritas tidak terdaftar di SIMO dan sudah tidak bisa ditemukan atau didapatkan lagi nomor identitas anggota/NIK (no. KTP), maka bisa menggunakan nomor sementara yang akan otomatis dibuatkan oleh sistem. 
      
      <br/><br/>
      Kemudian selanjutnya akan muncul tombol Tambah Anggota CU yang akan mengarahkan ke form untuk menambahkan anggota CU dan setelah selesai mengisi data anggota CU akan dibawa kembali ke form pengajuam bantuan solidaritas untuk melengkapi informasi detail terkait klaim.
    </p>

    <div class="mb-3 text-center">
      <img src="{{ asset('images/panduan/klaimJALINAN-tambah-pilih.png') }}" class="img-thumbnail" alt="">
    </div>

    <b>3. Menambah Bantuan Solidaritas Jalinan dengan memilih langsung dari daftar Anggota CU</b>
    <p>
      Cara ketiga adalah apabila sudah memiliki/mengisi data Anggota CU yang akan diajukan bantuan solidaritas di SIMO sebelumnya, maka tinggal memilih saja di tabel pada menu Anggota CU <span class="badge badge-pill bg-warning-400 ml-auto ml-md-0">1</span> kemudian menekan tombol Ajukan Bantuan Solidaritas Jalinan <span class="badge badge-pill bg-warning-400 ml-auto ml-md-0">2</span> maka akan langsung dibawa ke form bantuan solidaritas dan tinggal melengkapi informasi detail terkait klaim.
    </p>

    <div class="mb-3 text-center">
      <img src="{{ asset('images/panduan/klaimJALINAN-tambah.png') }}" class="img-thumbnail" alt="">
    </div>
    
    <p>Berikut adalah rincian masing-masing bagian pada form tambah klaim</p>

    <ol>
      <li><strong>Panel identitas anggota CU</strong> menampilkan identitas anggota CU</li>
      <li><strong>Pilih keanggotaan CU</strong> untuk memilih keanggotaan anggota CU</li>
      <li><strong>Pilih tipe</strong> untuk memilih tipe bantuan solidaritas (meninggal atau cacat)</li>
      <li><strong>Informasi produk</strong> untuk menampilkan informasi semua produk yang dimiliki oleh anggota tersebut</li>
      <li><strong>Form Jalinan</strong> untuk menampilkan form bantuan solidaritas yang mesti dilengkapi sesuai dengan keterangannya</li>
      <li><strong>Dokumen</strong> untuk mengirimkan dokumen-dokumen yang dibutuhkan sebagai persyaratan bantuan solidaritas seperti foto ktp, foto surat keterangan meninggal dunia dan sebagainya dalam bentuk format foto</li>
    </ol>

    <hr/> 
    <h6>B. Verifikasi Bantuan Solidaritas Jalinan </h6>
    <div class="mb-3 text-center">
      <img src="{{ asset('images/panduan/klaimJALINAN-verifi.png') }}" class="img-thumbnail" alt="">
    </div>
    <p>
      Setelah menambahkan klaim, maka hal selanjutnya adalah meminta kepada user yang memiliki hak akses untuk melakukan verifikasi bantuan solidaritas memeriksa dan melakukan verifikasi terhadap bantuan solidaritas tersebut.

      <br/><br/>
      Untuk verifikasi bantuan solidaritas Jalinan pengguna mesti memilih data yang akan dihapus dibagian konten terlebih dahulu baru kemudian menekan tombol verifikasi dan tombol verifikasi hanya akan muncul di user yang sudah ditentukan/diberitahukan kepada BKCU Kalimantan sebagai verifikator (biasanya 1 dari ketua pengurus, 1 dari ketua pengawas, dan 1 dari pimpinan manajemen/GM).

      <br/><br/>
      Setelah melakukan verifikasi ini baru lah pengajuan bantuan solidaritas akan diterima oleh bagian Jalinan PUSKOPCUINA untuk selanjutkan dilakukan analisis. (klaim tersebut akan berpindah dari tab verifikasi menjadi ke tab menunggu).
    </p>

    <div class="alert bg-warning text-white alert-styled-left alert-dismissible">
      <span class="font-weight-semibold">Penting!</span> Proses verifikasi mesti dilakukan untuk agar pengajuan bantuan solidaritas dapat diproses oleh bagian Jalinan di BKCU Kalimantan
    </div>

    <hr/> 
    <h6>C. Analisis Bantuan Solidaritas Jalinan - BKCU</h6>
    <div class="mb-3 text-center">
      <img src="{{ asset('images/panduan/klaimJALINAN-analisis.png') }}" class="img-thumbnail" alt="">
    </div>
    <p>
      Setelah bantuan solidaritas yang diajukan berada di tab menunggu, maka bagian Jalinan akan melakukan analisis terhadap bantuan solidaritas dan akan menentukan apakah bantuan solidaritas tersebut disetujui atau tidak.

      <br/><br/>
      Apabila bantuan solidaritas disetujui maka bagian Jalinan akan mengisi total Lintang dan Tunas yang disetujui dan sesuai dengan aturan yang tertuang pada Manual Operasional (MO) Jalinan PUSKOPCUINA
    </p>

    <div class="alert bg-warning text-white alert-styled-left alert-dismissible">
      <span class="font-weight-semibold">Penting!</span> Proses ini dilakukan oleh bagian Jalinan PUSKOPCUINA, jadi CU hanya perlu menunggu proses analisis saja apakah akan disetujui atau ditolak atau dokumen tidak lengkap dengan melihat pada masing-masing tab yang ada.
    </div>

    <hr/> 
    <h6>D. Selesai Bantuan Solidaritas Jalinan </h6>
    <div class="mb-3 text-center">
      <img src="{{ asset('images/panduan/klaimJALINAN-selesai.png') }}" class="img-thumbnail" alt="">
    </div>
    <p>
        Ketika bantuan solidaritas sudah disetujui dan kemudian sudah dicairkan oleh PUSKOPCUINA, maka bantuan solidaritas tersebut akan berada di tab disetujui dan disana CU bisa menekan tombol selesai <span class="badge badge-pill bg-warning-400 ml-auto ml-md-0">1</span> yang menandakan bahwa bantuan solidaritas tersebut sudah diterima oleh CU dan telah diberikan kepada alih waris

        <br/><br/>
        Ketika sudah menekan tombol selesai maka bantuan solidaritas dinyatakan sudah selesai dan tidak bisa dilakukan perubahan apapun lagi.
    </p>
    
    <hr/> 
    <h6>E. Mengubah Bantuan Solidaritas Jalinan</h6>
    <p>
        Untuk mengubah identitas bantuan solidaritas Jalinan pengguna mesti memilih data yang akan diubah dibagian konten terlebih dahulu baru kemudian menekan tombol ubah.
    </p>

    <hr/> 
    <h6>F. Menghapus Bantuan Solidaritas Jalinan </h6>
    <p>
        Untuk menghapus bantuan solidaritas Jalinan pengguna mesti memilih data yang akan dihapus dibagian konten terlebih dahulu baru kemudian menekan tombol hapus.
    </p>

    <hr/> 
    <h6>G. Koreksi Bantuan Solidaritas Jalinan </h6>
    <div class="mb-3 text-center">
      <img src="{{ asset('images/panduan/klaimJALINAN-koreksi.png') }}" class="img-thumbnail" alt="">
    </div>
    <p>
        Koreksi merupakan proses yang mirip seperti dengan mengubah klaim, tetapi bedanya adalah jika mengubah bantuan solidaritas hanya bisa dilakukan ketika status bantuan solidaritas tersebut masih verifikasi CU dan belum diverifikasi oleh semua verifikator. 

        <br/><br/>
        Sedangkan koreksi ini hanya bisa dilakukan setelah melakukan verifikasi dan dapat digunakan apabila ingin melakukan perbaikan apabila ada kesalahan pada klaim.
    </p>

    <hr/> 
    <h6>F. Surat Bantuan Solidaritas Jalinan </h6>
    <div class="mb-3 text-center">
      <img src="{{ asset('images/panduan/klaimJALINAN-surat.png') }}" class="img-thumbnail" alt="">
